let age=30;

switch (age) {
  case age = 10:
     console.log("age is 10");
    break;
  case age = 20:
     console.log("age is 20");
    break;
  case age = 30:
    console.log("age is 30");
    break;
    case age = 40:
       console.log("age is 40");
      break;
    case age = 50:
      console.log("age is 50");
      break;
  default:
      console.log("age is not in range");
     break;
}
